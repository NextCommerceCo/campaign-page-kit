// Convert a free-form name into a valid campaign slug.
function slugify(s) {
    return String(s || '').toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/^-+|-+$/g, '');
}

// Filter + sort a templates.json manifest into the order the picker shows.
// Drops `hidden`, sorts by `priority` desc, name asc as tiebreak.
function selectableTemplates(manifest) {
    return (manifest && manifest.templates || [])
        .filter(t => !t.hidden)
        .sort((a, b) => {
            const dp = (b.priority || 0) - (a.priority || 0);
            return dp !== 0 ? dp : String(a.name || a.slug).localeCompare(String(b.name || b.slug));
        });
}

// Produce the next state of `_data/campaigns.json` after merging an upstream
// entry under `localSlug` and overriding the name with what the user provided.
function mergeCampaignEntry(localCampaigns, localSlug, upstreamEntry, name) {
    const base = upstreamEntry || { description: '' };
    return { ...localCampaigns, [localSlug]: { ...base, name } };
}

// Merge missing CLI scripts into a package.json object. Returns
// { pkg, added: [keys] }; does not mutate the input.
function mergeScripts(pkg) {
    const next = { ...pkg, scripts: { ...(pkg && pkg.scripts || {}) } };
    const added = [];
    for (const [k, v] of Object.entries(SCRIPTS)) {
        if (!next.scripts[k]) {
            next.scripts[k] = v;
            added.push(k);
        }
    }
    return { pkg: next, added };
}

// Apply an API key to a config.js source string. Returns the new string,
// or null if no `apiKey:` field was found.
function applyApiKey(configSource, apiKey) {
    const updated = configSource.replace(/apiKey:\s*['"].*?['"]/, `apiKey: '${apiKey.trim()}'`);
    return updated === configSource ? null : updated;
}

// kebab-case → camelCase: 'ai-context' → 'aiContext'.
function kebabToCamel(s) {
    return String(s).replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
}

// Validate a campaign slug. Returns an error message string or null.
function validateSlug(s) {
    const t = String(s || '').trim();
    if (!t) return 'slug cannot be empty';
    if (!/^[a-z0-9][a-z0-9-]*$/.test(t)) return 'lowercase letters, digits, hyphens only (must start with a letter or digit)';
    return null;
}

// Look up the destination spec for an --ai-context tool name. Returns null
// for unknown tools and for `none` (the explicit "don't write" sentinel).
function aiContextTarget(tool) {
    if (!Object.prototype.hasOwnProperty.call(AI_CONTEXT_TARGETS, tool)) return null;
    return AI_CONTEXT_TARGETS[tool];
}

// Compose the final file content for an AI context destination: optional
// frontmatter, the sentinel, then the upstream document body.
function buildAiContextContent(upstreamBody, target) {
    const sentinelBlock = `${AI_CONTEXT_SENTINEL}\n\n`;
    return target.frontmatter
        ? `${target.frontmatter}\n${sentinelBlock}${upstreamBody}`
        : `${sentinelBlock}${upstreamBody}`;
}

// Parse argv against a flag schema. Returns { values, errors }.
// Supports `--key value`, `--key=value`, and `--flag` (boolean true).
// Unknown flags and missing required values produce structured errors.
function parseArgs(argv, schema = FLAG_SCHEMA) {
    const values = {};
    const errors = [];
    let i = 0;
    while (i < argv.length) {
        const tok = argv[i];
        // Accept --long-flags and -short-flags (e.g. -h). A bare `-` or `--`,
        // or a `-N` digit token, is treated as a positional argument and
        // rejected — we don't take any positional input.
        const isLong  = typeof tok === 'string' && tok.startsWith('--') && tok.length > 2;
        const isShort = typeof tok === 'string' && !tok.startsWith('--') && tok.startsWith('-') && tok.length > 1 && /^-[a-zA-Z]/.test(tok);
        if (!isLong && !isShort) {
            errors.push({ code: 'INVALID_INPUT', message: `unexpected positional argument: ${tok}` });
            i++;
            continue;
        }
        const prefixLen = isLong ? 2 : 1;
        const eq = tok.indexOf('=');
        const rawKey = eq === -1 ? tok.slice(prefixLen) : tok.slice(prefixLen, eq);
        const inlineVal = eq === -1 ? null : tok.slice(eq + 1);
        const def = schema[rawKey];
        if (!def) {
            errors.push({ code: 'INVALID_INPUT', message: `unknown flag: --${rawKey}` });
            // Skip a following value-like token so we don't double-report it.
            if (inlineVal === null && argv[i + 1] !== undefined && !argv[i + 1].startsWith('--')) i++;
            i++;
            continue;
        }
        const targetKey = def.target || kebabToCamel(rawKey);
        if (def.type === 'boolean') {
            if (inlineVal === null || inlineVal === '' || inlineVal === 'true') {
                values[targetKey] = true;
            } else if (inlineVal === 'false') {
                values[targetKey] = false;
            } else {
                errors.push({ code: 'INVALID_INPUT', message: `--${rawKey} is a boolean flag and does not accept a value` });
            }
            i++;
        } else { // string
            let v = inlineVal;
            if (v === null) {
                v = argv[i + 1];
                if (v === undefined || (typeof v === 'string' && v.startsWith('--'))) {
                    errors.push({ code: 'INVALID_INPUT', message: `--${rawKey} requires a value` });
                    i++;
                    continue;
                }
                i += 2;
            } else {
                i++;
            }
            if (def.enum && !def.enum.includes(v)) {
                errors.push({ code: 'INVALID_INPUT', message: `--${rawKey} must be one of: ${def.enum.join(', ')}` });
                continue;
            }
            values[targetKey] = v;
        }
    }
    return { values, errors };
}

