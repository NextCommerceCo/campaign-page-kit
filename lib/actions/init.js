async function main(argv = process.argv.slice(2)) {
    const { values: flags, errors: parseErrors } = parseArgs(argv, FLAG_SCHEMA);

    if (flags.help) {
        process.stdout.write(HELP_TEXT);
        return EXIT.OK;
    }

    const json = !!flags.json;
    const dryRun = !!flags.dryRun;
    // --json suppresses clack UI and therefore also disables prompts (otherwise
    // they'd corrupt the JSON stream). --non-interactive is the explicit knob.
    const nonInteractive = !!flags.nonInteractive || json;
    const brandRoot = process.cwd();
    const result = newResult({ dryRun, brandRoot });

    function fail(exitCode, errs) {
        result.status = 'error';
        result.errors.push(...errs);
        if (json) emitJson(result);
        else for (const e of errs) logger.error(e.message);
        return exitCode;
    }

    if (parseErrors.length > 0) {
        return fail(EXIT.INVALID_INPUT, parseErrors);
    }

    if (flags.slug) {
        const err = validateSlug(flags.slug);
        if (err) return fail(EXIT.INVALID_INPUT, [{ code: 'INVALID_INPUT', message: `--slug: ${err}` }]);
    }
    if (flags.keepAiContext && !flags.aiContext) {
        result.warnings.push('--keep-ai-context has no effect without --ai-context');
    }

    let ui;
    if (json) {
        ui = makeNoopUi();
    } else {
        const clack = await import('@clack/prompts');
        ui = makeClackUi(clack);
    }

    if (!json) logger.banner();
    if (!json && !nonInteractive) {
        ui.intro('Next Campaign Page Kit — start a campaign');
    } else if (!json) {
        logger.info('campaign-init (non-interactive)');
    }

    // 1. package.json scripts ------------------------------------------------
    const packageJsonPath = path.join(brandRoot, 'package.json');
    if (fs.existsSync(packageJsonPath)) {
        const current = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
        const { pkg, added } = mergeScripts(current);
        if (added.length > 0 && !dryRun) {
            fs.writeFileSync(packageJsonPath, JSON.stringify(pkg, null, 2) + '\n', 'utf8');
        }
        result.writes.package_json_scripts_added = added;
        if (!json) {
            if (added.length > 0) ui.log.success(`added package.json scripts: ${D}${added.join(', ')}${R}`);
            else ui.log.step('package.json scripts already present');
        }
    } else {
        result.warnings.push('no package.json found — run `npm init -y` first');
        if (!json) ui.log.warn(`no package.json found — run ${D}npm init -y${R} first`);
    }

    // 2. _data/campaigns.json -----------------------------------------------
    const campaignsDataPath = path.join(brandRoot, '_data', 'campaigns.json');
    if (!fs.existsSync(campaignsDataPath)) {
        if (!dryRun) {
            fs.mkdirSync(path.dirname(campaignsDataPath), { recursive: true });
            fs.writeFileSync(campaignsDataPath, CAMPAIGNS_JSON, 'utf8');
        }
        result.writes.campaigns_json_seeded = true;
        if (!json) ui.log.success('created _data/campaigns.json');
    } else if (!json) {
        ui.log.step('_data/campaigns.json already exists');
    }

    // 3-4. resolve template slug + source -----------------------------------
    // Source is caller-driven, not family-driven: by default we fetch from the
    // public starter-templates repo (anonymous). The --source-* / --private flags
    // point at a different repo/ref/subtree — e.g. an outside, access-controlled
    // family. A --private source is fetched authenticated and is NOT expected in
    // any public templates.json, so it skips the picker and resolves only by an
    // explicit --template slug. page-kit holds no per-family source knowledge.
    let templateSlug = flags.template;
    let source;
    try {
        source = resolveTemplateSource({
            repo: flags.sourceRepo,
            ref: flags.sourceRef,
            source_path: flags.sourcePath,
            private: flags.private,
        });
    } catch (err) {
        // resolveTemplateSource validates --source-* shape (repo/ref/path) and
        // throws INVALID_SOURCE on a malformed value (traversal, odd chars, etc.).
        return fail(EXIT.INVALID_INPUT, [{ code: 'INVALID_SOURCE', message: err.message }]);
    }
    let token = null;
    let pickedTemplate;

    if (source.private) {
        // Private sources are unlisted: an explicit --template slug is required
        // (no picker), and a token is required to fetch the private tarball.
        if (!templateSlug) {
            return fail(EXIT.MISSING_INPUT, [{
                code: 'MISSING_INPUT',
                message: 'a --private source requires an explicit --template <slug> (private sources are not shown in the picker)',
            }]);
        }
        token = resolveGithubToken();
        if (!token) {
            // A missing token is a missing precondition, not a fetch failure — no
            // fetch was attempted. Exit 5 (MISSING_INPUT) is the contract for
            // "scripted run lacks required input"; the code string stays specific
            // so a caller can tell this apart from a missing --template.
            return fail(EXIT.MISSING_INPUT, [{
                code: 'MISSING_GITHUB_TOKEN',
                message: `--private source ${source.repo} needs a GitHub token; set GITHUB_TOKEN/GH_TOKEN or run \`gh auth login\` to fetch it`,
            }]);
        }
        // Synthetic descriptor. The display name defaults to the slug here, then
        // is upgraded to the source repo's campaigns.json name after step 8 (when
        // no explicit --name was passed) — see "refine display name" below.
        pickedTemplate = { slug: templateSlug, name: templateSlug, description: `private template source (${source.repo})` };
    } else {
        let manifest;
        try {
            manifest = await fetchJson(source.templates_url);
        } catch (err) {
            return fail(EXIT.UPSTREAM_FETCH_FAILED, [{
                code: 'UPSTREAM_FETCH_FAILED',
                message: `could not fetch templates.json: ${err.message}`,
            }]);
        }
        const templates = selectableTemplates(manifest);
        if (templates.length === 0) {
            return fail(EXIT.UPSTREAM_FETCH_FAILED, [{
                code: 'UPSTREAM_FETCH_FAILED',
                message: 'upstream templates.json had no selectable templates',
            }]);
        }

        if (templateSlug) {
            if (!templates.find(t => t.slug === templateSlug)) {
                return fail(EXIT.UPSTREAM_TEMPLATE_NOT_FOUND, [{
                    code: 'UPSTREAM_TEMPLATE_NOT_FOUND',
                    message: `template "${templateSlug}" not found in upstream templates.json`,
                    hint: `valid: ${templates.map(t => t.slug).join(', ')}`,
                }]);
            }
        } else if (nonInteractive) {
            return fail(EXIT.MISSING_INPUT, [{
                code: 'MISSING_INPUT',
                message: '--template is required in --non-interactive mode',
                hint: `valid: ${templates.map(t => t.slug).join(', ')}`,
            }]);
        } else {
            const picked = await ui.select({
                message: 'Pick a starter template',
                options: templates.map(t => ({
                    value: t.slug,
                    label: t.deprecated ? `[DEPRECATED] ${t.name}` : t.name,
                    hint: t.description || t.slug,
                })),
            });
            if (ui.isCancel(picked)) { ui.outro('Cancelled.'); return EXIT.OK; }
            templateSlug = picked;
        }
        pickedTemplate = templates.find(t => t.slug === templateSlug);
    }

    // 5. resolve name -------------------------------------------------------
    let finalName = flags.name && String(flags.name).trim();
    if (!finalName) {
        if (nonInteractive) {
            finalName = pickedTemplate.name;
        } else {
            const entered = await ui.text({
                message: `Campaign name ${D}(display name shown in your dev server picker)${R}`,
                placeholder: pickedTemplate.name,
                initialValue: pickedTemplate.name,
                validate: (v) => { if (!(v || '').trim()) return 'name cannot be empty'; },
            });
            if (ui.isCancel(entered)) { ui.outro('Cancelled.'); return EXIT.OK; }
            finalName = entered.trim();
        }
    }

    // 6. resolve slug -------------------------------------------------------
    let finalSlug = flags.slug && String(flags.slug).trim();
    if (!finalSlug) {
        const defaultSlug = slugify(finalName) || templateSlug;
        if (nonInteractive) {
            finalSlug = defaultSlug;
        } else {
            const entered = await ui.text({
                message: `Campaign slug ${D}(directory name under src/ and URL path: e.g. /my-campaign/)${R}`,
                placeholder: defaultSlug,
                initialValue: defaultSlug,
                validate: (v) => validateSlug(v),
            });
            if (ui.isCancel(entered)) { ui.outro('Cancelled.'); return EXIT.OK; }
            finalSlug = entered.trim();
        }
    }

    // 7. conflict check -----------------------------------------------------
    const destDir = path.join(brandRoot, 'src', finalSlug);
    let localCampaigns;
    try {
        localCampaigns = JSON.parse(fs.readFileSync(campaignsDataPath, 'utf8'));
    } catch (err) {
        return fail(EXIT.GENERIC, [{
            code: 'GENERIC',
            message: `could not read _data/campaigns.json: ${err.message}`,
        }]);
    }
    const hasDirectory = fs.existsSync(destDir);
    const hasRegistryEntry = Object.prototype.hasOwnProperty.call(localCampaigns, finalSlug);
    if (hasDirectory || hasRegistryEntry) {
        const parts = [hasDirectory && `src/${finalSlug}/`, hasRegistryEntry && `_data/campaigns.json[${finalSlug}]`].filter(Boolean);
        if (nonInteractive) {
            if (!flags.overwrite) {
                return fail(EXIT.CONFLICT, [{
                    code: 'CONFLICT',
                    message: `${parts.join(' and ')} already exists`,
                    hint: 're-run with --overwrite to replace',
                }]);
            }
        } else {
            const ok = await ui.confirm({ message: `${parts.join(' and ')} already exists. Overwrite?`, initialValue: false });
            if (ui.isCancel(ok) || !ok) { ui.outro('Cancelled.'); return EXIT.OK; }
        }
    }

    // 8. fetch upstream registry entry (best effort) ------------------------
    let upstreamEntry = null;
    try {
        const upstream = source.private
            ? await fetchJsonAuthed(source.campaigns_url, token)
            : await fetchJson(source.campaigns_url);
        upstreamEntry = upstream[templateSlug] || null;
        if (!upstreamEntry) {
            const msg = `upstream registry has no entry for ${templateSlug}; using a minimal local entry`;
            result.warnings.push(msg);
            if (!json) ui.log.warn(msg);
        }
    } catch (err) {
        const msg = `could not fetch upstream registry entry: ${err.message}; using a minimal local entry`;
        result.warnings.push(msg);
        if (!json) ui.log.warn(msg);
    }

    // Refine display name: a --private source's synthetic descriptor defaults its
    // name to the slug (it isn't in any public manifest). When the operator did
    // not pass --name, prefer the source repo's campaigns.json name (e.g. a
    // proper-cased title instead of the raw slug). finalSlug is already fixed, so
    // this only affects the display name, never the directory/URL.
    if (source.private && !flags.name && upstreamEntry && upstreamEntry.name) {
        const upstreamName = String(upstreamEntry.name).trim();
        if (upstreamName) finalName = upstreamName;
    }

    const nextCampaigns = mergeCampaignEntry(localCampaigns, finalSlug, upstreamEntry, finalName);

    result.campaign = {
        slug: finalSlug,
        name: finalName,
        template: templateSlug,
        sdk_version: (upstreamEntry && upstreamEntry.sdk_version) || null,
    };
    result.writes.campaigns_json_entry = finalSlug;
    result.writes.destination = `src/${finalSlug}/`;

    // 9. dry-run early exit -------------------------------------------------
    if (dryRun) {
        if (flags.aiContext && flags.aiContext !== 'none') {
            try {
                const written = await writeAiContext({
                    brandRoot, tool: flags.aiContext, keep: !!flags.keepAiContext, dryRun: true,
                });
                if (written) result.writes.ai_context.push({ tool: flags.aiContext, ...written });
            } catch (err) {
                result.warnings.push(`AI context (dry-run): ${err.message}`);
            }
        }
        if (json) emitJson(result);
        else if (!nonInteractive) ui.outro('Dry run — no writes performed.');
        else logger.info('dry run — no writes performed');
        return EXIT.OK;
    }

    // 10. download + extract + atomic replace -------------------------------
    const sp = ui.spinner();
    sp.start(`Downloading ${templateSlug}`);
    let stagingRoot;
    try {
        const destParent = path.dirname(destDir);
        fs.mkdirSync(destParent, { recursive: true });
        stagingRoot = fs.mkdtempSync(path.join(destParent, '.cpk-install-'));
        const stagedDir = path.join(stagingRoot, finalSlug);
        // Private families need an authenticated tarball (api.github.com tarball
        // endpoint → signed redirect); public families use the anonymous
        // codeload URL. fetchBufferAuthed drops the token on the signed redirect.
        const tarball = source.private
            ? await fetchBufferAuthed(source.tarball_url_authenticated, token)
            : await fetchBuffer(source.tarball_url_anonymous);
        const written = extractTemplate(tarball, templateSlug, stagedDir, source.source_path);
        replaceDirectoryWithRollback(stagedDir, destDir, () => {
            fs.writeFileSync(campaignsDataPath, JSON.stringify(nextCampaigns, null, 2) + '\n', 'utf8');
        });
        sp.stop(`Wrote ${written} files to src/${finalSlug}/`);
        result.writes.files_extracted = written;
    } catch (err) {
        sp.stop('Template install failed');
        return fail(EXIT.UPSTREAM_FETCH_FAILED, [{
            code: 'UPSTREAM_FETCH_FAILED',
            message: `template install failed: ${err.message}`,
        }]);
    } finally {
        if (stagingRoot) {
            fs.rmSync(stagingRoot, { recursive: true, force: true });
        }
    }
    if (!json) ui.log.success(`Registered ${D}${finalSlug}${R} in _data/campaigns.json`);

    // 11. API key -----------------------------------------------------------
    let apiKey = flags.apiKey && String(flags.apiKey).trim();
    if (!apiKey && !nonInteractive) {
        const wantsKey = await ui.confirm({ message: 'Set the Campaign API key now?', initialValue: true });
        if (!ui.isCancel(wantsKey) && wantsKey) {
            const entered = await ui.text({
                message: `API key ${D}(from your store's Campaigns App — written to assets/config.js)${R}`,
                placeholder: 'your-api-key',
                validate: (v) => { if (!(v || '').trim()) return 'API key cannot be empty'; },
            });
            if (!ui.isCancel(entered)) apiKey = String(entered).trim();
        }
    }
    if (apiKey) {
        const configPath = path.join(destDir, 'assets', 'config.js');
        try {
            const updated = applyApiKey(fs.readFileSync(configPath, 'utf8'), apiKey);
            if (updated === null) {
                const msg = 'no apiKey field found in assets/config.js — set it manually';
                result.warnings.push(msg);
                if (!json) ui.log.warn(msg);
            } else {
                fs.writeFileSync(configPath, updated, 'utf8');
                result.writes.api_key_applied = true;
                if (!json) ui.log.success('API key written to assets/config.js');
            }
        } catch (err) {
            const msg = `could not write API key: ${err.message}`;
            result.warnings.push(msg);
            if (!json) ui.log.warn(msg);
        }
    }

    // 12. AI context --------------------------------------------------------
    // Interactive flow:
    //   1. Ask "Set up AI context?"
    //   2. If yes, pick a tool
    //   3. If that tool's destination file already exists, confirm update
    // Non-interactive: only --ai-context <tool> drives this (no prompts).
    const aiContextTools = [];
    if (flags.aiContext && flags.aiContext !== 'none') {
        aiContextTools.push(flags.aiContext);
    } else if (!nonInteractive) {
        const wants = await ui.confirm({
            message: `Set up AI context now? ${D}(installs CLAUDE.md / AGENTS.md / etc. for your editor or agent)${R}`,
            initialValue: false,
        });
        if (!ui.isCancel(wants) && wants) {
            const picked = await ui.select({
                message: 'Which tool?',
                options: [
                    { value: 'claude',  label: 'Claude Code',    hint: 'CLAUDE.md' },
                    { value: 'codex',   label: 'OpenAI Codex',   hint: 'AGENTS.md' },
                    { value: 'cursor',  label: 'Cursor',         hint: '.cursor/rules/campaign-page-kit.mdc' },
                    { value: 'copilot', label: 'GitHub Copilot', hint: '.github/copilot-instructions.md' },
                ],
            });
            if (!ui.isCancel(picked)) {
                const target = aiContextTarget(picked);
                const exists = fs.existsSync(path.join(brandRoot, target.path));
                if (exists) {
                    const update = await ui.confirm({
                        message: `${target.path} already exists. Update it?`,
                        initialValue: false,
                    });
                    if (!ui.isCancel(update) && update) aiContextTools.push(picked);
                } else {
                    aiContextTools.push(picked);
                }
            }
        }
    }

    if (aiContextTools.length > 0) {
        for (const tool of aiContextTools) {
            try {
                const written = await writeAiContext({
                    brandRoot, tool, keep: !!flags.keepAiContext, dryRun: false,
                });
                if (written) {
                    result.writes.ai_context.push({ tool, ...written });
                    if (!json) ui.log.success(`AI context (${tool}): ${written.action} ${written.path}`);
                }
            } catch (err) {
                // Non-fatal: scaffold itself succeeded.
                const msg = `AI context (${tool}): ${err.message}`;
                result.warnings.push(msg);
                if (!json) ui.log.warn(msg);
            }
        }
    }

    // 13. emit final result -------------------------------------------------
    if (json) {
        emitJson(result);
    } else if (!nonInteractive) {
        ui.outro(`Next step: ${D}npm run dev${R} — preview ${finalSlug}`);
    } else {
        logger.info(`done — preview with: npm run dev (pick ${finalSlug})`);
    }

    return EXIT.OK;
}

if (require.main === module) {
    main().then(
        (code) => process.exit(code || 0),
        (err) => {
            logger.error(err && err.message || String(err));
            process.exit(EXIT.GENERIC);
        }
    );
}

