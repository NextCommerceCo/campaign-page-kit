// ---------------------------------------------------------------------------
// AI context file write
// ---------------------------------------------------------------------------

async function writeAiContext({ brandRoot, tool, keep, dryRun }) {
    const target = aiContextTarget(tool);
    if (!target) return null;

    const fullPath = path.join(brandRoot, target.path);
    const exists = fs.existsSync(fullPath);

    if (exists && keep) {
        return { path: target.path, action: 'skipped', reason: '--keep-ai-context' };
    }
    if (dryRun) {
        return { path: target.path, action: exists ? 'would-overwrite' : 'would-create', reason: '--dry-run' };
    }

    let upstreamBody;
    try {
        const buf = await fetchBuffer(AI_CONTEXT_DOC_URL);
        upstreamBody = buf.toString('utf8');
    } catch (err) {
        const wrapped = new Error(`could not fetch AI context doc: ${err.message}`);
        wrapped.code = 'UPSTREAM_FETCH_FAILED';
        throw wrapped;
    }

    const content = buildAiContextContent(upstreamBody, target);
    fs.mkdirSync(path.dirname(fullPath), { recursive: true });
    fs.writeFileSync(fullPath, content, 'utf8');
    return { path: target.path, action: exists ? 'overwritten' : 'created' };
}

